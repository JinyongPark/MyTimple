package visang.timple.model;

public class TeamMemberDTO {
	private String teamid;
	private String projectseq;
	private String memseq;
	private String grade;
	private String praticipation;
	
	public String getTeamid() {
		return teamid;
	}
	public void setTeamid(String teamid) {
		this.teamid = teamid;
	}
	public String getProjectseq() {
		return projectseq;
	}
	public void setProjectseq(String projectseq) {
		this.projectseq = projectseq;
	}
	public String getMemseq() {
		return memseq;
	}
	public void setMemseq(String memseq) {
		this.memseq = memseq;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getPraticipation() {
		return praticipation;
	}
	public void setPraticipation(String praticipation) {
		this.praticipation = praticipation;
	}
	
	
}

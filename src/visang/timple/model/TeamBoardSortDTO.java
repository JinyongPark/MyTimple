package visang.timple.model;

public class TeamBoardSortDTO {

	private String teamBoardSortId;
	private String projectSeq;
	private String subject;
	private String sort;
		
	public String getTeamBoardSortId() {
		return teamBoardSortId;
	}
	public void setTeamBoardSortId(String teamBoardSortId) {
		this.teamBoardSortId = teamBoardSortId;
	}
	public String getProjectSeq() {
		return projectSeq;
	}
	public void setProjectSeq(String projectSeq) {
		this.projectSeq = projectSeq;
	}
	
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	
}
